package com.Threenumber;

public class Greatest {
	 public static void main(String[] args) 
	    {
	        int x=5;
	        int y=8;
	        int z=3;
	        
	        if(x > y && x > z)
	        {
	            System.out.println("Largest number is:"+x);
	        }
	        else if(y > z)
	        {
	            System.out.println("Largest number is:"+y);
	        }
	        else
	        {
	            System.out.println("Largest number is:"+z);
	        }
	 
	    }
	}

