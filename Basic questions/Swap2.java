package com.Threenumber;

public class Swap2 {
	public static void main(String arg[]) {  
		    
		  int a = 20;  
		  int b = 30;  
		  System.out.println("Before swapping");
		  System.out.println("value of x:" + a);  
		  System.out.println("value of y:" + b);  
		  a=a+b;  
		  b=a-b;  
		  a=a-b; 
		  System.out.println("After swapping"); 
		  System.out.println("value of x:" + a);  
		  System.out.println("value of y:" + b);  
		 }  
		}  

